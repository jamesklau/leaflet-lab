//Represents a GeoJSON object or an array of GeoJSON objects. Allows you to parse GeoJSON data and display it on the map
var geojsonMarkerOptions = {
    radius: 8,
    fillColor: "#ff7800",
    color: "#000",
    weight: 1,
    opacity: 1,
    fillOpacity: 0.8
};

